// twofish_mcrypt.go implements the game's non-standard Twofish-128-CBC.
//
// The Feistel rounds, whitening, q-box layer count, and key split all match
// standard Twofish for 128-bit keys. The only confirmed deviation is the
// column-2 MDS table used during T-table generation and the h-function:
//
// Standard col2(q) = [mul5B(q), mulEF(q), q,         mulEF(q)]  (LE bytes)
// Game     col2(q) = [(mul5B(q)+q), mulEF(q), 0, mulEF(q)]  (integer add)
//
// The binary still applies the standard 3-layer q-box structure for both the
// key-dependent T-tables and the subkey h-function; the custom col2 is applied
// only at the final MDS-combine step.

import (
	"crypto/cipher"
	"math/bits"
	"unsafe"

	"golang.org/x/crypto/twofish"
)

