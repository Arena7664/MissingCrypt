// tfRS is the Reed-Solomon matrix used in Twofish key schedule.
var tfRS = [4][8]byte{
	{0x01, 0xA4, 0x55, 0x87, 0x5A, 0x58, 0xDB, 0x9E},
	{0xA4, 0x56, 0x82, 0xF3, 0x1E, 0xC6, 0x68, 0xE5},
	{0x02, 0xA1, 0xFC, 0xC1, 0x47, 0xAE, 0x3D, 0x19},
	{0xA4, 0x55, 0x87, 0x5A, 0x58, 0xDB, 0x9E, 0x03},
}

// tfGFMult returns a·b in GF(2^8)/p (branchless).
func tfGFMult(a, b byte, p uint32) byte {
	B := [2]uint32{0, uint32(b)}
	P := [2]uint32{0, p}
	var result uint32
	for i := 0; i < 7; i++ {
		result ^= B[a&1]
		a >>= 1
		B[1] = P[B[1]>>7] ^ (B[1] << 1)
	}
	result ^= B[a&1]
	return byte(result)
}

// tfMDSCol computes the standard MDS column multiply for col ∈ {0,1,3}.
func tfMDSCol(q byte, col int) uint32 {
	mul5B := tfGFMult(q, 0x5B, 0x169)
	mulEF := tfGFMult(q, 0xEF, 0x169)
	switch col {
	case 0:
		return uint32(q) | uint32(mul5B)<<8 | uint32(mulEF)<<16 | uint32(mulEF)<<24
	case 1:
		return uint32(mulEF) | uint32(mulEF)<<8 | uint32(mul5B)<<16 | uint32(q)<<24
	case 3:
		return uint32(mul5B) | uint32(q)<<8 | uint32(mulEF)<<16 | uint32(mul5B)<<24
	}
	panic("tfMDSCol: invalid col")
}

// tfMDSCol2Game computes the game's non-standard col2:
// byte0 = integer(mul5B + q), byte1 = mulEF (+carry), byte2 = 0+carry, byte3 = mulEF.
func tfMDSCol2Game(q byte) uint32 {
	mul5B := tfGFMult(q, 0x5B, 0x169)
	mulEF := tfGFMult(q, 0xEF, 0x169)
	return uint32(mul5B) + uint32(q) + uint32(mulEF)<<8 + uint32(mulEF)<<24
}

// tfGameH computes the game's h function for 128-bit keys.
// It preserves standard Twofish's 3-layer q-box structure and swaps only the
// final MDS col2 combine for the game's custom col2 table.
func tfGameH(key []byte, counterVal uint32, useOdd bool) uint32 {
	var lo, hi int
	if useOdd {
		lo, hi = 4, 12
	} else {
		lo, hi = 0, 8
	}
	b0 := byte(counterVal)
	b1 := byte(counterVal >> 8)
	b2 := byte(counterVal >> 16)
	b3 := byte(counterVal >> 24)

	r0 := tfSBoxes[1][key[lo+0]^tfSBoxes[0][key[hi+0]^tfSBoxes[0][b0]]]
	r1 := tfSBoxes[0][key[lo+1]^tfSBoxes[0][key[hi+1]^tfSBoxes[1][b1]]]
	r2 := tfSBoxes[1][key[lo+2]^tfSBoxes[1][key[hi+2]^tfSBoxes[0][b2]]]
	r3 := tfSBoxes[0][key[lo+3]^tfSBoxes[1][key[hi+3]^tfSBoxes[1][b3]]]

	return tfMDSCol(r0, 0) ^ tfMDSCol(r1, 1) ^ tfMDSCol2Game(r2) ^ tfMDSCol(r3, 3)
}

// tfInternals mirrors the unexported twofish.Cipher struct layout so we can
// patch s[0..3] and k[0..39] after standard key expansion.
// twofish.Cipher is: s [4][256]uint32, k [40]uint32 — stable across versions.
type tfInternals struct {
	s [4][256]uint32
	k [40]uint32 //nolint:unused
}

// newMcryptTwofishCipher creates a Twofish block cipher with the game's modified
// key schedule and MDS col2 table. The Feistel round structure and I/O format
// are reused from the standard golang.org/x/crypto/twofish implementation by
// patching the key-dependent tables in place via unsafe.Pointer.
func newMcryptTwofishCipher(key []byte) (cipher.Block, error) {
	// Build a standard cipher first to obtain the round function and struct layout;
	// we will overwrite all key-dependent state below.
	c, err := twofish.NewCipher(key)
	if err != nil {
		return nil, err
	}

	// Derive S-bytes S[0..7] from the key via RS over GF(2^8)/0x14d.
	// For 128-bit key (numWords=2): S[0..3]=RS·key[0..7], S[4..7]=RS·key[8..15].
	numWords := len(key) / 8
	var S [8]byte
	for i := 0; i < numWords; i++ {
		for j := 0; j < 4; j++ {
			for l := 0; l < 8; l++ {
				S[4*i+j] ^= tfGFMult(key[8*i+l], tfRS[j][l], 0x14d)
			}
		}
	}

	tf := (*tfInternals)(unsafe.Pointer(c))

	// Patch all four T-tables using standard 128-bit Twofish q-box depth with
	// the game's custom col2 MDS table:
	//   T0[i] = col0_std( q1[S[4] ^ q0[S[0] ^ q0[i]]] )
	//   T1[i] = col1_std( q0[S[5] ^ q0[S[1] ^ q1[i]]] )
	//   T2[i] = col2_game( q1[S[6] ^ q1[S[2] ^ q0[i]]] )
	//   T3[i] = col3_std( q0[S[7] ^ q1[S[3] ^ q1[i]]] )
	for i := 0; i < 256; i++ {
		b := byte(i)
		t0 := tfSBoxes[1][S[4]^tfSBoxes[0][S[0]^tfSBoxes[0][b]]]
		t1 := tfSBoxes[0][S[5]^tfSBoxes[0][S[1]^tfSBoxes[1][b]]]
		t2 := tfSBoxes[1][S[6]^tfSBoxes[1][S[2]^tfSBoxes[0][b]]]
		t3 := tfSBoxes[0][S[7]^tfSBoxes[1][S[3]^tfSBoxes[1][b]]]
		tf.s[0][i] = tfMDSCol(t0, 0)
		tf.s[1][i] = tfMDSCol(t1, 1)
		tf.s[2][i] = tfMDSCol2Game(t2)
		tf.s[3][i] = tfMDSCol(t3, 3)
	}

	// Patch all 40 subkeys using the game's h function.
	for i := 0; i < 20; i++ {
		counterA := uint32(i) * 0x02020202
		A := tfGameH(key, counterA, false)
		B := bits.RotateLeft32(tfGameH(key, counterA+0x01010101, true), 8)
		tf.k[2*i] = A + B
		tf.k[2*i+1] = bits.RotateLeft32(A+2*B, 9)
	}

	return c, nil
}
